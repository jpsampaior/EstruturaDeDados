package aaa;

public class Treinador {
    int valorAtaque;
    int valorDefesa;
    int valorTreinador;
    int valorGolpeTotal;

    public int getValorGolpeTotal() {
        return valorGolpeTotal;
    }

    public void setValorGolpeTotal(int valorGolpeTotal) {
        this.valorGolpeTotal = valorGolpeTotal;
    }

    public int getValorAtaque() {
        return valorAtaque;
    }

    public void setValorAtaque(int valorAtaque) {
        this.valorAtaque = valorAtaque;
    }

    public int getValorDefesa() {
        return valorDefesa;
    }

    public void setValorDefesa(int valorDefesa) {
        this.valorDefesa = valorDefesa;
    }

    public int getValorTreinador() {
        return valorTreinador;
    }

    public void setValorTreinador(int valorTreinador) {
        this.valorTreinador = valorTreinador;
    }
}
